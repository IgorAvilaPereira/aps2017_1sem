    private void jButtonCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCancelarActionPerformed
        List<JCheckBox> pratoprincipal = new ArrayList<>();
        List<JCheckBox> saladas = new ArrayList<>();
        for( Component comp : jPanelPratoPrincipal.getComponents() ) {
            if( comp instanceof JCheckBox) pratoprincipal.add( (JCheckBox)comp );
        }
        for( Component comp : jPanelSaladas.getComponents() ) {
            if( comp instanceof JCheckBox) saladas.add( (JCheckBox)comp );
        }
        for(JCheckBox boxpp : pratoprincipal){
            if(boxpp.isSelected()){
                boxpp.setSelected(false);
            }
        }
        for(JCheckBox boxsa : saladas){
            if(boxsa.isSelected()){
                boxsa.setSelected(false);
           }
        }
        combosobremesa.setSelectedIndex(0);
        jCheckBoxTxServico.setSelected(false);
        jTextFieldDesconto.setText("0");
        jTextAreaTicket.setText("");
        SpinnerAgua.setValue(((SpinnerNumberModel) SpinnerAgua.getModel()).getMinimum());
        SpinnerCerveja.setValue(((SpinnerNumberModel) SpinnerCerveja.getModel()).getMinimum());
        SpinnerRefri.setValue(((SpinnerNumberModel) SpinnerRefri.getModel()).getMinimum());
        SpinnerSuco.setValue(((SpinnerNumberModel) SpinnerSuco.getModel()).getMinimum());
        
    }//GEN-LAST:event_jButtonCancelarActionPerformed

