    private void jButtonCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCalcularActionPerformed
        List<JCheckBox> pratoprincipal = new ArrayList<>();
        List<JCheckBox> saladas = new ArrayList<>();
        List<String> info = new ArrayList<>();
        for( Component comp : jPanelPratoPrincipal.getComponents() ) {
            if( comp instanceof JCheckBox) pratoprincipal.add( (JCheckBox)comp );
        }
        for( Component comp : jPanelSaladas.getComponents() ) {
            if( comp instanceof JCheckBox) saladas.add( (JCheckBox)comp );
        }

        for(JCheckBox boxpp : pratoprincipal){
            if(boxpp.isSelected()){
                info.add(boxpp.getText());
            }
        }
        for(JCheckBox boxsa : saladas){
            if(boxsa.isSelected()){
                info.add(boxsa.getText());
           }
        }
        cupom.ContaComida(info);
        cupom.ContaTudo();
        jTextAreaTicket.setText(cupom.Escreve());
        System.out.println(jTextAreaTicket.getText());
        try {
            new TicketDAO().inserir(jTextAreaTicket.getText().replace("\n", "|"));
        } catch (SQLException ex) {
            Logger.getLogger(TelaComanda.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButtonCalcularActionPerformed

